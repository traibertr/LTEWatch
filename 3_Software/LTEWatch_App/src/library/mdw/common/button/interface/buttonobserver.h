namespace gpio
{
    
    /* ************************************************************************************************************ */
    /*                                                                                                              */
    /* Class Declaration                                                                                            */
    /*                                                                                                              */
    /* ************************************************************************************************************ */
    class Button;
    class ButtonObserver
    {
    public:
        /* ******************************************************************************************************** */
        /*                                                                                                          */
        /* PUBLIC DECLARATION SECTION                                                                               */
        /*                                                                                                          */
        /* ******************************************************************************************************** */
        /********************************************************************************************************//**
         * \name    ButtonObservers 
         * @brief   Callback methods for the Button's event
         * \{
         ************************************************************************************************************/
        // ----------------------------------------------------------------------------------------------------------
        virtual void onButtonReleased(Button* button) = 0;

        // ----------------------------------------------------------------------------------------------------------
        virtual void onButtonSingleClick(Button* button) = 0;

        // ----------------------------------------------------------------------------------------------------------
        virtual void onButtonDoubleClick(Button* button) = 0;

        // ----------------------------------------------------------------------------------------------------------
        virtual void onButtonTripleClick(Button* button) = 0;

        // ----------------------------------------------------------------------------------------------------------
        virtual void onButtonLongPress(Button* button) = 0;

        // ----------------------------------------------------------------------------------------------------------
        virtual void onButtonDoubleLongPress(Button* button) = 0;

        // ----------------------------------------------------------------------------------------------------------
        virtual void onButtonTripleLongPress(Button* button) = 0;

        // ----------------------------------------------------------------------------------------------------------
        virtual void onButtonVeryLongPress(Button* button) = 0;
        /** \} */
    };

}   // namespace gpio

